public class TypeCasting {
    public static void main(String[] args) {

        // Change the integer data type to float
        int my_int = 92;
        float myFloat = my_int;
        System.out.println(myFloat); // result  = 92.0


        // Change the float data type to integer
        float myFloat2 = 78.64F;
        int myInt2 = (int) myFloat2;
        System.out.println(myInt2); // reuslt = 78

    }
}
